package Mate_01_Java_Basics.Boolean;

public class ComparisonOperators {
    public void setBooleans(int a, int b) {
        boolean isEqual = a == b;
        boolean isNot = a != b;
        boolean isLess = a < b;
        boolean isGreater = a > b;
        boolean isLessOrEqual = a <= b;
        boolean isGreaterOrEqual = a >= b;
    }
}
